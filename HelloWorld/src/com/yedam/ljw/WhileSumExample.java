package com.yedam.ljw;

public class WhileSumExample {

	public static void main(String[] args) {
		
		/*****************************************/
		
		// 1~5 합을 구하는 식
		int sum = 0;
		int i = 1;
		while(i<=5) {
			sum = sum + 1;
			i++;
		}
		System.out.println(sum);
		/*****************************************/
	}

}
